    private void view() {
        Save.setVisible(false);
        EntityManager em = ENTITY_MANAGER_FACTORY.createEntityManager();
        String query = "SELECT c FROM Smlouva c WHERE c.nsmlouva = :nsmlouva";

        // Issue the query and get a matching Customer
        TypedQuery<Smlouva> tq = em.createQuery(query, Smlouva.class);
        tq.setParameter("nsmlouva", nsmlouva);

        Smlouva cust = null;
        try {
            // Get matching customer object and output
            cust = tq.getSingleResult();
            Integer intInstance = cust.getNsmlouva();
            NSmlouvyText.setText(intInstance.toString());
            NSmlouvyText.setEditable(false);
            intInstance = cust.getIcolekarna();
            IcoLekText.setText(intInstance.toString());
            IcoLekText.setEditable(false);
            intInstance = cust.getIcofarmspol();
            IcoSpolText.setText(intInstance.toString());
            IcoSpolText.setEditable(false);
            PrijemceText.setText(cust.getPrijemce());
            PrijemceText.setEditable(false);
            DodavatelText.setText(cust.getDodavatel());
            DodavatelText.setEditable(false);
            ZacatekText.setText(cust.getZacatek());
            ZacatekText.setEditable(false);
            KonecText.setText(cust.getKonec());
            KonecText.setEditable(false);

        } catch(Exception e) {}
    }

