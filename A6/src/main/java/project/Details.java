    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        EntityManager em = ENTITY_MANAGER_FACTORY.createEntityManager();
        EntityTransaction et = null;
        Smlouva cu = null;

        try {
            et = em.getTransaction();
            et.begin();
            cu = em.find(Smlouva.class, nsmlouva);
            em.remove(cu);
            et.commit();
        } catch (Exception ex) {
            // If there is an exception rollback changes
            if (et != null) {
                et.rollback();
            }
            ex.printStackTrace();
        } finally {
            // Close EntityManager
            em.close();
            JOptionPane.showMessageDialog(null, "Smlouva smazana uspesne");
            this.cust = cust;
            HomePage hm = new HomePage(cust);
            hm.setVisible(true);
            hm.pack();
            hm.setLocationRelativeTo(null);
            hm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        }
    }//GEN-LAST:event_RemoveActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        Save.setVisible(true);
        Remove.setVisible(false);
        EntityManager em = ENTITY_MANAGER_FACTORY.createEntityManager();
        EntityTransaction et = null;

        Smlouva cu = null;
        try {
            et = em.getTransaction();
            et.begin();
            // Get matching customer object and output
            cu = em.find(Smlouva.class, nsmlouva);

            cu.setNsmlouva(Integer.parseInt(NSmlouvyText.getText()));
            cu.setIcolekarna(Integer.parseInt(IcoLekText.getText()));
            cu.setIcofarmspol(Integer.parseInt(IcoSpolText.getText()));
            cu.setPrijemce(PrijemceText.getText());
            cu.setDodavatel(DodavatelText.getText());
            cu.setZacatek(ZacatekText.getText());
            cu.setKonec(KonecText.getText());
            em.persist(cu);
            et.commit();
        } catch(Exception e) {
            if (et != null) {
                et.rollback();
            }
            e.printStackTrace();
        } finally {
            // Close EntityManager
            em.close();
            JOptionPane.showMessageDialog(null, "Zmena provedla uspesne");
            this.cust = cust;
            HomePage hm = new HomePage(cust);
            hm.setVisible(true);
            hm.pack();
            hm.setLocationRelativeTo(null);
            hm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        }
    }//GEN-LAST:event_SaveActionPerformed

