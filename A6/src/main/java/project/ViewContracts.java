    private void HledatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HledatActionPerformed
        EntityManager em = ENTITY_MANAGER_FACTORY.createEntityManager();
        EntityTransaction et = null;

        if(icolekarnaText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pole je prazdna");
            if (et != null) {
                et.rollback();
            }     
        } else {
            int icolekarna = Integer.parseInt(icolekarnaText.getText());
            index = new ArrayList<>();
            list.clear();
            //the lowercase c refers to the object
            //:custID is a parameterized query thats value is set below
            String strQuery = "SELECT c FROM Smlouva c WHERE c.icolekarna = :icolekarna";
            
            //Issue the query and get a matching Customer
            TypedQuery<Smlouva> tq = em.createQuery(strQuery, Smlouva.class);

            tq.setParameter("icolekarna", icolekarna);
            
            List<Smlouva> custs;

            try {
                //Get matching customer object and output
                custs = tq.getResultList();
                for(int i=0; i<custs.size(); i++) {
                    if(cust.getIcolekarna() == custs.get(i).getIcolekarna()) {
                        list.add(custs.get(i).getNsmlouva() + "        " + custs.get(i).getIcolekarna()+ "       " + custs.get(i).getIcofarmspol() + "        " + custs.get(i).getZacatek() + "        " + custs.get(i).getKonec() + "      " + custs.get(i).getPrijemce() + "      " + custs.get(i).getDodavatel());
                        index.add(custs.get(i).getNsmlouva());
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "Nemate pristup");
                        et.rollback();
                    }
                }
                if(custs.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Zadana smlouva neni nalezena");
                    if (et != null) {
                        et.rollback();
                    }     
                }
            } catch(Exception e) {
            }
            finally {
                em.close();
            }
        }
    }//GEN-LAST:event_HledatActionPerformed

